export function getStats() {
  return data.reduce(
    (result, current) => ({
      minRating: Math.min(result.minRating, current.rating),
      maxRating: Math.max(result.maxRating, current.rating),
      minPopularity: Math.min(result.minPopularity, current.popularity),
      maxPopularity: Math.max(result.maxPopularity, current.popularity),
      minPlays: Math.min(result.minPlays, current.nbPlays),
      maxPlays: Math.max(result.maxPlays, current.nbPlays),
    }),
    {
      minRating: data[0]?.rating,
      maxRating: data[0]?.rating,
      minPopularity: data[0]?.popularity,
      maxPopularity: data[0]?.popularity,
      minPlays: data[0]?.nbPlays,
      maxPlays: data[0]?.nbPlays,
    }
  );
}

export function getThemes() {
  return data
    .reduce((uniqueThemes, current) => {
      for (const theme of current.themes) {
        if (!uniqueThemes.includes(theme)) {
          uniqueThemes.push(theme);
        }
      }

      return uniqueThemes;
    }, [])
    .sort();
}

